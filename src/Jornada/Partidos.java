    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        estadios estadio=new estadios();
        int idPartido = Integer.parseInt(txtIdpartido.getText());
        int NoJornada = Integer.parseInt(txtNojornada.getText());
        String EquipoLocal = txtEquipolocal.getText();
        String Equivisitante = txtEquipovisitante.getText();
        int GolLocal = Integer.parseInt(txtGollocal.getText());
        int GolVisditante = Integer.parseInt(txtGolvisitante.getText());
        int dia = Integer.parseInt(txtDia.getText());
        String mes = txtMes.getText();
        int anio = Integer.parseInt(txtAnio.getText());
        String Hora = txtHora.getText();
        String duracion = txtDuracion.getText();
        int score = Integer.parseInt(txtScore.getText());

    
        
        ConexionSQL conexion = new ConexionSQL();
        conexion.getConexion();
        Connection conn = conexion.getConexion();
        try {

            // PreparedStatement ps = con2.prepareStatement ("INSERT INTO Equipo values (IdEquipo, Nombre, Mascota, ColorPlayera)");
            PreparedStatement ps = conn.prepareStatement("INSERT INTO Partido values (?,?,?,?,?,?,?,?,?,?,?,?,?)");
            ps.setInt(1, idPartido);
            ps.setInt(2, NoJornada);
            ps.setString(3, EquipoLocal);
            ps.setString(4, Equivisitante);
            ps.setInt(5, GolLocal);
            ps.setInt(6, GolVisditante);
            ps.setInt(7, dia);
            ps.setString(8, mes);
            ps.setInt(9, anio);
            ps.setString(10, Hora);
            ps.setString(11, duracion);
            ps.setInt(12, score);
ps.setInt(13, idPartido);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Registro guardado");

        } catch (SQLException e) {
            Logger.getLogger(Jugador.class.getName()).log(Level.SEVERE, null, e);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

